def get_exercises_by_category(category):
    """Get exercises for a specific muscle group category"""
    return EXERCISE_CATEGORIES.get(category.lower(), [])


def search_exercises(search_term):
    """Search for exercises containing the search term"""
    search_term = search_term.lower()
    return [exercise for exercise in TRAINERIZE_EXERCISES if search_term in exercise.lower()]


def validate_exercise(exercise_name):
    """Check if an exercise exists in the Trainerize exercise list"""
    return exercise_name in TRAINERIZE_EXERCISES
